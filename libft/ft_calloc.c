#include <stdlib.h>
#include "libft.h"

/*
** Allocates memory for "count" objects, each of size "size" and fills with 0's.
*/

void	*ft_calloc(size_t count, size_t size)
{
	void	*mem;

	if (count == 0 || size == 0)
	{
		count = 1;
		size = 1;
	}
	mem = malloc(count * size);
	if (mem)
		ft_memset(mem, 0, count * size);
	return (mem);
}
